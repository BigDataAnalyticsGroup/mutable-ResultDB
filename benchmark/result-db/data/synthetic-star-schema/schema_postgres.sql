DROP TABLE IF EXISTS Fact, Dim1, Dim2, Dim3, Dim4;

CREATE TABLE Dim1 (
    id INTEGER PRIMARY KEY,
    a INTEGER NOT NULL,
    b CHAR(16) NOT NULL
);

CREATE TABLE Dim2 (
    id INTEGER PRIMARY KEY,
    a INTEGER NOT NULL,
    b CHAR(16) NOT NULL
);

CREATE TABLE Dim3 (
    id INTEGER PRIMARY KEY,
    a INTEGER NOT NULL,
    b CHAR(16) NOT NULL
);

CREATE TABLE Dim4 (
    id INTEGER PRIMARY KEY,
    a INTEGER NOT NULL,
    b CHAR(16) NOT NULL
);

CREATE TABLE Fact (
    id INTEGER PRIMARY KEY,
    fkd1 INTEGER NOT NULL REFERENCES Dim1(id),
    fkd2 INTEGER NOT NULL REFERENCES Dim2(id),
    fkd3 INTEGER NOT NULL REFERENCES Dim3(id),
    fkd4 INTEGER NOT NULL REFERENCES Dim4(id),
    a INTEGER NOT NULL,
    b CHAR(16) NOT NULL
);
